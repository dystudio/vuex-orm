import { createStore, createState } from 'test/support/Helpers'
import Model from 'app/model/Model'

describe('Features – Relations – Has Many', () => {
  class User extends Model {
    static entity = 'users'

    static fields () {
      return {
        id: this.attr(null),
        posts: this.hasMany(Post, 'user_id')
      }
    }
  }

  class Post extends Model {
    static entity = 'posts'

    static fields () {
      return {
        id: this.attr(null),
        user_id: this.attr(null)
      }
    }
  }

  it('can create data containing the has many relation', async () => {
    const store = createStore([{ model: User }, { model: Post }])

    await User.create({
      id: 1,
      posts: [
        { id: 1 },
        { id: 2 }
      ]
    })

    const expected = createState({
      users: {
        1: { $id: '1', id: 1, posts: [] }
      },
      posts: {
        1: { $id: '1', id: 1, user_id: 1 },
        2: { $id: '2', id: 2, user_id: 1 }
      }
    })

    expect(store.state.entities).toEqual(expected)
  })

  it('can update data and insert has many relation', async () => {
    const store = createStore([{ model: User }, { model: Post }])

    await User.create({ id: 1 })

    await User.update(
      { id: 1, posts: [{ id: 1 }, { id: 2 }] },
      { insert: ['posts'] }
    )

    const expected = createState({
      users: {
        1: { $id: '1', id: 1, posts: [] }
      },
      posts: {
        1: { $id: '1', id: 1, user_id: 1 },
        2: { $id: '2', id: 2, user_id: 1 }
      }
    })

    expect(store.state.entities).toEqual(expected)
  })

  it('can resolve the has many relation', async () => {
    createStore([{ model: User }, { model: Post }])

    await User.create([
      {
        id: 1,
        posts: [
          { id: 1 },
          { id: 2 }
        ]
      },
      {
        id: 2,
        posts: null
      }
    ])

    const expected = [
      {
        $id: '1',
        id: 1,
        posts: [
          { $id: '1', id: 1, user_id: 1 },
          { $id: '2', id: 2, user_id: 1 }
        ]
      },
      {
        $id: '2',
        id: 2,
        posts: []
      }
    ]

    const user = User.query().with('posts').all()

    expect(user).toEqual(expected)
  })

  it('can resolve the has many relation with custom primary key', async () => {
    class User extends Model {
      static entity = 'users'

      static primaryKey = 'user_id'

      static fields () {
        return {
          user_id: this.attr(null),
          posts: this.hasMany(Post, 'user_id')
        }
      }
    }

    createStore([{ model: User }, { model: Post }])

    await User.create({
      user_id: 1,
      posts: [
        { id: 1 },
        { id: 2 }
      ]
    })

    const expected = {
      $id: '1',
      user_id: 1,
      posts: [
        { $id: '1', id: 1, user_id: 1 },
        { $id: '2', id: 2, user_id: 1 }
      ]
    }

    const user = User.query().with('posts').find(1)

    expect(user).toEqual(expected)
  })

  it('can resolve the has many using the local key', () => {
    class User extends Model {
      static entity = 'users'

      static fields () {
        return {
          id: this.attr(),
          local_key: this.attr(),
          posts: this.hasMany(Post, 'user_id', 'local_key')
        }
      }
    }

    class Post extends Model {
      static entity = 'posts'

      static fields () {
        return {
          id: this.attr(),
          user_id: this.attr(null)
        }
      }
    }

    const store = createStore([{ model: User }, { model: Post }])

    User.create({
      id: 1,
      local_key: 'local_key',
      posts: [{ id: 1 }]
    })

    const expected = createState({
      users: {
        1: { $id: '1', id: 1, local_key: 'local_key', posts: [] }
      },
      posts: {
        1: { $id: '1', id: 1, user_id: 'local_key' }
      }
    })

    expect(store.state.entities).toEqual(expected)
  })
})
